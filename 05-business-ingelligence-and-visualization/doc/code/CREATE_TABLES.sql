CREATE TABLE CLIENTE
(
  CODIGO_LOCAL VARCHAR(50) PRIMARY KEY
, NOMBRE_LOCAL VARCHAR(200)
, CODIGO_POSTAL BIGINT
, POBLACION VARCHAR(50)
, PROVINCIA VARCHAR(50)
, OFICINA VARCHAR(10)
)
;


CREATE TABLE PROYECTO
(
  CODIGO_PROYECTO VARCHAR(50) PRIMARY KEY
)
;


CREATE TABLE AUDITOR
(
  CODIGO_AUDITOR VARCHAR(50) PRIMARY KEY
)
;

CREATE TABLE EVALUACION
(
  ID_EVALUACION BIGINT PRIMARY KEY
, TITULO VARCHAR(200)
, FECHA DATETIME
)
;


CREATE TABLE ENCUESTA
(
  ID_ENCUESTA BIGINT AUTO_INCREMENT NOT NULL PRIMARY KEY
, CLIENTE VARCHAR(50)
, PROYECTO VARCHAR(50)
, AUDITOR VARCHAR(50)
, EVALUACION BIGINT
, RESULTADO DOUBLE
)
;
